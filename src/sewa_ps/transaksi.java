    private void penomoran_otomatis() {
        int kode_tr=0;
        String temp_kode_tr="";
        try {
            koneksi();
            String sql = "select kd_transaksi from transaksi order by kd_transaksi desc limit 1";                        
            ResultSet res=stt.executeQuery(sql);
            while (res.next()) {
                temp_kode_tr = res.getString(1);                
                String nomor_blkg = temp_kode_tr.substring(3,5);
                kode_tr=Integer.parseInt(nomor_blkg);
            }
                        
            kode_tr++;
            String nilai=String.valueOf(kode_tr);                      
            if(nilai.length()==1){
                a_kdtransaksi.setText("TR00"+kode_tr);
            }else
            if(nilai.length()==2){
                a_kdtransaksi.setText("TR0"+kode_tr);
            }else
            if(nilai.length()==3){
                a_kdtransaksi.setText("TR"+kode_tr);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Error Penomoran !");
        }
    }
    
    

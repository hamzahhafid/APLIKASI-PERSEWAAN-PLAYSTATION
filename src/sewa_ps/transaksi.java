    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
        if(a_kdtransaksi.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Pilih Data Terlebih Dahulu !");
        }
        else{
            int reply = JOptionPane.showConfirmDialog(this,"Hapus Data Pemeriksaan ?","Warning", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (reply == JOptionPane.YES_OPTION) {
                try {
                    java.sql.Connection koneksi = (java.sql.Connection) DriverManager.getConnection("jdbc:mysql://localhost/sewa_ps","root","");
                    koneksi.createStatement().executeUpdate("DELETE FROM transaksi where kd_transaksi='"+a_kdtransaksi.getText()+"'");
                    JOptionPane.showMessageDialog(this, "Data Dihapus !");
                    tampilkan_data();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Data Gagal Dihapus !");
                }

            }
        }
    }//GEN-LAST:event_hapusActionPerformed

    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        //Simpan Data
        String kd_tr = a_kdtransaksi.getText();
        String kd_rental = (String) cbrental.getSelectedItem();
        String nm = a_nm.getText();
        String tot = a_totrental.getText();
        String id_member = a_idmember.getText();
        String diskon = a_potongan.getText();
        String total = a_total.getText();
        String bayar = a_bayar.getText();
        String kembali = a_kembali.getText();
        String ket = (String) a_ket.getSelectedItem();
        {
            try
            {
                Class.forName("com.mysql.jdbc.Driver").newInstance();
                com.mysql.jdbc.Connection koneksi = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://127.0.0.1/sewa_ps", "root", "");
                com.mysql.jdbc.Statement statement = (com.mysql.jdbc.Statement) koneksi.createStatement();
                String sql="insert into transaksi values('"+kd_tr+"','"+kd_rental+"','"+nm+"','"+tot+"','"+id_member+"','"+diskon+"','"+total+"','"+bayar+"','"+kembali+"','"+ket+"')";
                int executeUpdate = statement.executeUpdate(sql);
                statement.close();
                JOptionPane.showMessageDialog(null, "Data berhasil dimasukkan..","Insert Data",JOptionPane.INFORMATION_MESSAGE);
                a_kdtransaksi.setText("");
                a_totrental.setText("");
                a_nm.setText("");
                a_potongan.setText("");
                a_total.setText("");
                a_bayar.setText("");
                a_kembali.setText("");
                a_idmember.setText("");
                koneksi.close();
            }
            catch (     ClassNotFoundException | InstantiationException | IllegalAccessException | SQLException | HeadlessException e)
            {
                JOptionPane.showMessageDialog(null, "Eror: "+e,"Gagal",JOptionPane.WARNING_MESSAGE);
                //System.err.println("Exception: "+e.getMessage());
            }
            finally {
                tampilkan_data();
            }
        }
    }//GEN-LAST:event_simpanActionPerformed

